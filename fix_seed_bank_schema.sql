-- Ejecutar en el SQL Editor de Supabase
-- IMPORTANTE: Ejecutar paso a paso si hay errores

-- 1. Crear la tabla si no existe
CREATE TABLE IF NOT EXISTS seed_bank (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  owner_key TEXT NOT NULL,
  nombre TEXT NOT NULL,
  especie TEXT,
  cantidad INT DEFAULT 0,
  fecha_ingreso TIMESTAMP WITH TIME ZONE,
  origen TEXT DEFAULT 'propia',
  cross_id BIGINT,
  estado TEXT DEFAULT 'almacenada',
  inicio_estratificacion TIMESTAMP WITH TIME ZONE,
  fin_estratificacion TIMESTAMP WITH TIME ZONE,
  notas TEXT
);

-- 2. Agregar columnas si faltan (para tablas existentes)
ALTER TABLE seed_bank ADD COLUMN IF NOT EXISTS owner_key TEXT;
ALTER TABLE seed_bank ADD COLUMN IF NOT EXISTS especie TEXT;
ALTER TABLE seed_bank ADD COLUMN IF NOT EXISTS cross_id BIGINT;
ALTER TABLE seed_bank ADD COLUMN IF NOT EXISTS inicio_estratificacion TIMESTAMP WITH TIME ZONE;
ALTER TABLE seed_bank ADD COLUMN IF NOT EXISTS fin_estratificacion TIMESTAMP WITH TIME ZONE;

