-- 3. Habilitar RLS
ALTER TABLE seed_bank ENABLE ROW LEVEL SECURITY;

-- 4. Crear políticas de seguridad (RLS)
DROP POLICY IF EXISTS "Owner Access SeedBank" ON seed_bank;
CREATE POLICY "Owner Access SeedBank"
  ON seed_bank FOR ALL
  USING ( owner_key = (SELECT key FROM access_keys WHERE device_id = auth.uid() LIMIT 1) )
  WITH CHECK ( owner_key = (SELECT key FROM access_keys WHERE device_id = auth.uid() LIMIT 1) );

-- Política de respaldo para insertar
DROP POLICY IF EXISTS "Enable insert for authenticated users only" ON seed_bank;
CREATE POLICY "Enable insert for authenticated users only"
  ON seed_bank FOR INSERT
  WITH CHECK ( auth.role() = 'authenticated' );

-- 5. Crear índice para mejorar performance
CREATE INDEX IF NOT EXISTS idx_seed_bank_owner_key ON seed_bank(owner_key);
CREATE INDEX IF NOT EXISTS idx_seed_bank_cross_id ON seed_bank(cross_id);
